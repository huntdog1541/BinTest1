
public class Areg {
	
	//8-bit Lower
	char[] AL = new char[8];
	char[] AH = new char[8];
	
	char[] AX = new char[16];
	
	char[] EAX = new char[32];
	
	char[] RAX = new char[64];

	
	public char[] getAL() {
		return AL;
	}

	public void setAL(char[] aL) {
		AL = aL;
	}

	public char[] getAH() {
		return AH;
	}

	public void setAH(char[] aH) {
		AH = aH;
	}

	public char[] getAX() {
		return AX;
	}

	public void setAX(char[] aX) {
		AX = aX;
	}

	public char[] getEAX() {
		return EAX;
	}

	public void setEAX(char[] eAX) {
		EAX = eAX;
	}

	public char[] getRAX() {
		return RAX;
	}

	public void setRAX(char[] rAX) {
		RAX = rAX;
	}
	
	
}
